package com.foodregulation.dao.entity;

public class PunishBasis {
    private Integer punishBasisId;

    private String punishBasisCode;

    private String punishBasisContent;

    public Integer getPunishBasisId() {
        return punishBasisId;
    }

    public void setPunishBasisId(Integer punishBasisId) {
        this.punishBasisId = punishBasisId;
    }

    public String getPunishBasisCode() {
        return punishBasisCode;
    }

    public void setPunishBasisCode(String punishBasisCode) {
        this.punishBasisCode = punishBasisCode;
    }

    public String getPunishBasisContent() {
        return punishBasisContent;
    }

    public void setPunishBasisContent(String punishBasisContent) {
        this.punishBasisContent = punishBasisContent;
    }
}